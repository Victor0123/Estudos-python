a = int(input("Digite o primeiro valor: "))
b = int(input("Digite o segundo valor: "))
operacao = input("Operação matematica: ")

if operacao == "adição":
    c = a + b
    print(c)

if operacao == "subtração":
    c = a - b
    print(c)

if operacao == "multiplicação":
    c = a * b
    print(c)

if operacao == "divisão":
    c = a / b
    print(c)
